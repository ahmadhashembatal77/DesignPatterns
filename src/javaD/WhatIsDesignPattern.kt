class DesignPattern{ /** Design Pattern
     * Design patterns are typical solutions to commonly occurring
     * problems in software design. They are like pre-made blueprints
     * that you can customize to solve a recurring design problem in your code.
     *
     * The pattern is not a specific piece of code, but a general concept
     * for solving a particular problem. You can follow the pattern
     * details and implement a solution that suits the realities of your
     * own program
     *
     * This book covers three main groups of patterns:
     *
     * • Creation Design Patterns : provide object creation mechanisms that
     *   increase flexibility and reuse of existing code.
     *
     * • Structural patterns : explain how to assemble objects and
     *   classes into larger structures, while keeping the structures flexible
     *   and efficient.
     *
     * • Behavioral patterns : take care of effective communication and
     *   the assignment of responsibilities between objects
     * */ }

